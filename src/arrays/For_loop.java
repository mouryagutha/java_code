package arrays;

import java.util.Scanner;

public class For_loop {
    public static void main(String[] args) {
//        Scanner sc = new Scanner(System.in);

        int n = 6;

        for(int i=1; i<n ; i++)
        {
            for(int j =i; j<n ; j++)
            {
                System.out.println(j);
            }
        }
    }
}
